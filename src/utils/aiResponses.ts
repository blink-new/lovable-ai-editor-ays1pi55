// ردود ذكية محاكية من الذكاء الاصطناعي
export const getAIResponse = (userMessage: string): string => {
  const message = userMessage.toLowerCase()
  
  // تحليل نوع المشروع المطلوب
  if (message.includes('متجر') || message.includes('تجارة') || message.includes('بيع')) {
    return `رائع! سأقوم بإنشاء متجر إلكتروني احترافي لك يتضمن:
    
• واجهة عرض المنتجات مع صور عالية الجودة
• نظام سلة التسوق وإدارة الطلبات  
• بوابة دفع آمنة متعددة الطرق
• لوحة تحكم لإدارة المتجر
• تصميم متجاوب للجوال والحاسوب
• نظام إدارة المخزون

هل تريد إضافة أي ميزات خاصة؟`
  }
  
  if (message.includes('تطبيق') || message.includes('موبايل') || message.includes('جوال')) {
    return `ممتاز! سأطور تطبيق جوال قوي يشمل:

• تصميم عصري وسهل الاستخدام
• واجهات تفاعلية سريعة الاستجابة
• نظام تسجيل دخول وإدارة الحسابات
• إشعارات فورية للمستخدمين
• قاعدة بيانات محسّنة
• تزامن البيانات عبر الأجهزة

هل لديك فكرة محددة عن وظائف التطبيق؟`
  }
  
  if (message.includes('موقع') || message.includes('ويب') || message.includes('انترنت')) {
    return `ممتاز! سأبني موقع ويب احترافي يحتوي على:

• تصميم جذاب ومتجاوب
• صفحات تحميل سريعة
• تحسين محركات البحث (SEO)
• نماذج تفاعلية
• قسم المدونة والأخبار
• صفحة تواصل متطورة

ما هو الهدف الرئيسي من الموقع؟`
  }
  
  if (message.includes('لعبة') || message.includes('جيم') || message.includes('تسلية')) {
    return `رائع! سأصمم لعبة ممتعة تتضمن:

• رسومات عالية الجودة
• أصوات وموسيقى تصويرية
• مستويات متدرجة الصعوبة
• نظام نقاط وإنجازات
• وضع اللعب الفردي والجماعي
• حفظ التقدم والإحصائيات

أي نوع من الألعاب تفضل؟`
  }
  
  if (message.includes('تعليم') || message.includes('دورة') || message.includes('كورس')) {
    return `ممتاز! سأنشئ منصة تعليمية شاملة تشمل:

• نظام إدارة المحتوى التعليمي
• فيديوهات تفاعلية وعروض تقديمية
• اختبارات وتقييمات ذكية
• متابعة تقدم الطلاب
• شهادات إتمام معتمدة
• منتدى نقاش للطلاب

ما هو المجال التعليمي المستهدف؟`
  }
  
  if (message.includes('إدارة') || message.includes('أعمال') || message.includes('شركة')) {
    return `ممتاز! سأطور نظام إدارة متقدم يحتوي على:

• لوحة تحكم تحليلية شاملة
• إدارة الموظفين والمهام
• نظام إدارة العملاء (CRM)
• تقارير مالية تفصيلية
• نظام الفواتير والمحاسبة
• إدارة المشاريع والمواعيد

ما هو حجم الشركة ونوع الصناعة؟`
  }
  
  // رد افتراضي
  return `شكراً لك على هذه الفكرة الرائعة! سأبدأ في تطوير مشروعك على الفور.

سأقوم بإنشاء تطبيق متكامل يتضمن:
• تصميم جذاب وعصري
• واجهة مستخدم سهلة وبديهية  
• وظائف متقدمة وفعالة
• أمان عالي وحماية البيانات
• دعم لجميع الأجهزة
• إمكانيات توسع مستقبلية

هل تريد إضافة أي متطلبات خاصة أو تعديلات على المشروع؟`
}

